package Percobaan;

public class OperatingSystem {
    private String os;

    public OperatingSystem() {
        this.os = "Windows";
    }

    public OperatingSystem(String os) {
        this.os = os;
    }

    public void setOs(String os) {
        this.os = os;
    }

    public String getOs() {
        return os;
    }
}